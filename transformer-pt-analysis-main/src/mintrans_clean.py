def plot_training_curves(history: Dict, title: str = "") -> None:
    fig, (ax_loss, ax_acc) = plt.subplots(1, 2, figsize=(12, 4))
    epochs = range(1, len(history["train_loss"]) + 1)
    suffix = f" — {title}" if title else ""


def _make_pair_split(
    mod: int, train_frac: float = 0.8, seed: int = 42
) -> Tuple[List[Tuple[int, int]], List[Tuple[int, int]]]:
    """Randomly partition all mod² pairs into (train_seeds, val_seeds)."""
    all_pairs = [(a, b) for a in range(mod) for b in range(mod)]
    rng = random.Random(seed)
    rng.shuffle(all_pairs)
    n_train = int(train_frac * len(all_pairs))
    return all_pairs[:n_train], all_pairs[n_train:]


class FibonacciTrainDataset(Dataset):
    """
    Training dataset — Nanda et al. format: [a, b, =] → predict c at position 2.

    Each sample is (x, y) where:
      x = [a, b, eq]   — input sequence of length 3
      y = [b, eq, c]   — targets shifted one step right (c = (a+b) % mod)
    Loss is computed only at the last position (predicting c from [a, b, =]).

    Attributes
    ----------
    seen_pairs : set of (int, int)
        The (a, b) seed pairs used for training.  Passed to FibonacciValDataset
        to guarantee that validation pairs are held out.
    """

    def __init__(
        self,
        mod: int = 113,
        seq_len: int = 3,
        train_frac: float = 0.3,
        seed: int = 42,
    ) -> None:
        self.mod = mod
        self.seq_len = seq_len
        eq = mod                 # "=" token lives at index p

        train_seeds, self._val_seeds = _make_pair_split(mod, train_frac, seed)

        self.seen_pairs: Set[Tuple[int, int]] = set()
        self.samples: List[Tuple[torch.Tensor, torch.Tensor]] = []

        for (a, b) in train_seeds:
            c = (a + b) % mod
            x = torch.tensor([a, b, eq], dtype=torch.long)
            y = torch.tensor([b, eq, c], dtype=torch.long)
            self.seen_pairs.add((a, b))
            self.samples.append((x, y))

    def __len__(self) -> int:
        return len(self.samples)

    def __getitem__(self, idx: int) -> Tuple[torch.Tensor, torch.Tensor]:
        return self.samples[idx]


class FibonacciValDataset(Dataset):
    """
    Validation dataset — Nanda et al. format: [a, b, =] → predict c at position 2.

    Each validation pair (a, b) is guaranteed not to appear in the training set,
    so the corresponding prediction c = (a+b) % mod is strictly held out.

    Parameters
    ----------
    train_ds    : the FibonacciTrainDataset to test against
    num_samples : number of validation sequences to generate
    seed        : random seed
    """

    def __init__(
        self,
        train_ds: FibonacciTrainDataset,
        num_samples: int = 500,
        seed: int = 0,
    ) -> None:
        mod  = train_ds.mod
        eq   = mod               # "=" token lives at index p
        seen = train_ds.seen_pairs

        unseen = [
            (a, b) for a in range(mod) for b in range(mod)
            if (a, b) not in seen
        ]
        if not unseen:
            raise ValueError(
                "No unseen pairs available for validation. "
                "Lower train_frac."
            )

        rng = random.Random(seed)
        self.samples: List[Tuple[torch.Tensor, torch.Tensor]] = []

        for _ in range(num_samples):
            a, b = rng.choice(unseen)
            c = (a + b) % mod
            x = torch.tensor([a, b, eq], dtype=torch.long)
            y = torch.tensor([b, eq, c], dtype=torch.long)
            self.samples.append((x, y))

    def __len__(self) -> int:
        return len(self.samples)

    def __getitem__(self, idx: int) -> Tuple[torch.Tensor, torch.Tensor]:
        return self.samples[idx]



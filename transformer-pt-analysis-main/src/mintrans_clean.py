@dataclass
class TrainConfig:
    """All hyperparameters in one place for easy experimentation."""
    p: int = 113                # prime modulus; Nanda et al. use 113
    vocab_size: int = p+1       # p + 1: tokens 0..p-1 plus "=" at index p
    d_model: int = 128
    n_heads: int = 4
    num_layers: int = 1
    hidden_mlp: int = 512
    seq_len: int = 3            # [a, b, =] — Nanda et al. format
    batch_size: int = 1000
    lr: float = 1e-3
    weight_decay: float = 1  # drives grokking (see diagnostic_metrics.tex §7); LR decay controls oscillations
    epochs: int = 15000
    train_frac: float = 0.3     # fraction of all p² pairs used for training
    monitor_every: int = 50     # epochs between spectral snapshots (0 = off)
    checkpoint_path: Optional[str] = None
    # Reproducibility: set data_seed=0 and torch_seed=0 to match Nanda et al.'s
    # default Config (seed=0 for pair-split; torch seed is not fixed in his code
    # but 0 is a clean baseline for comparison).
    data_seed: int = 0         # seed for the train/val pair split
    torch_seed: int = 0         # seed for model weight initialisation


# ---------------------------------------------------------------------------
# Utility: Fibonacci recurrence
# ---------------------------------------------------------------------------

def _fib_forward(seed: Tuple[int, int], length: int, mod: int) -> List[int]:
    """Return a Fibonacci sequence mod p of `length` starting from seed (a, b)."""
    seq = list(seed)
    while len(seq) < length:
        seq.append((seq[-1] + seq[-2]) % mod)
    return seq


def _fib_backward(terminal: Tuple[int, int], total_length: int, mod: int) -> List[int]:
    """
    Build a Fibonacci sequence of `total_length` whose last two elements are
    `terminal`.  Uses the backward recurrence x_{t-2} = x_t - x_{t-1} (mod p).
    """
    seq = list(terminal)
    while len(seq) < total_length:
        seq.insert(0, (seq[1] - seq[0]) % mod)
    return seq



"""
Minimal Transformer trained on modular Fibonacci sequences.

Reproduces and extends the grokking experiments from:
  Nanda et al. (2023) "Progress measures for grokking via mechanistic interpretability"
  https://arxiv.org/abs/2301.05217

Task: given a sequence governed by x_{t+1} = (x_t + x_{t-1}) mod p, predict the
next token autoregressively.  The model must discover that only the last two tokens
determine the next one, and ideally encode them via trigonometric functions at a
small set of key frequencies.

Dataset split guarantee
-----------------------
Training sequences are generated by extending randomly chosen seed pairs forward.
Every consecutive pair (x_i, x_{i+1}) appearing in any training sequence is
recorded in FibonacciTrainDataset.seen_pairs.  Validation sequences are then
constructed so that their terminal pair — (x[T-2], x[T-1]), the pair that
uniquely determines the next prediction — does not appear in seen_pairs.

For seq_len=2 (Nanda's setup) this is an exact pair-level split: every pair
appears in exactly one sequence, so seen_pairs = train seeds.  For seq_len > 2
intermediate pairs within a validation sequence may coincide with training pairs
(because the Fibonacci orbit chains pairs together), but the final prediction
task is always held out.
"""


# Training loop
# ---------------------------------------------------------------------------

def _loss_and_acc(
    logits: torch.Tensor, targets: torch.Tensor
) -> Tuple[torch.Tensor, float]:
    """
    Cross-entropy loss and accuracy evaluated only at the last position.

    In the Nanda format [a, b, =] → [b, =, c], only position 2 (predicting c
    from context [a, b, =]) is used in the loss.  Positions 0 and 1 are
    skipped: they involve predicting b and "=" which carry no information about
    generalisation of the modular-addition rule.
    """
    logits_flat  = logits[:, -1, :]   # [B, vocab_size]
    targets_flat = targets[:, -1]     # [B]
    # Compute loss in float64: float32 log_softmax underflows on confident logits,
    # quantising gradients to multiples of ~1.2e-7 and producing dodgy gradient
    # estimates that cause limit-cycle oscillations (Nanda et al. use the same fix).
    logprobs = F.log_softmax(logits_flat.double(), dim=-1)
    loss = -logprobs.gather(1, targets_flat.unsqueeze(1)).squeeze(1).mean()
    acc  = (logits_flat.argmax(-1) == targets_flat).float().mean().item()
    return loss, acc


@torch.no_grad()
def evaluate(model: nn.Module, loader: DataLoader) -> Tuple[float, float]:
    """Return (mean_loss, mean_accuracy) over the full DataLoader."""
    model.eval()
    total_loss = total_acc = n = 0
    for x, y in loader:
        x, y = x.to(device), y.to(device)
        loss, acc = _loss_and_acc(model(x), y)
        total_loss += loss.item(); total_acc += acc; n += 1
    return total_loss / n, total_acc / n


def train_model(
    model: nn.Module,
    train_loader: DataLoader,
    val_loader: DataLoader,
    cfg: TrainConfig,
) -> Dict:
    """
    Train and return a history dict:
      "train_loss", "val_loss", "train_acc", "val_acc" — one value per epoch
      "spectral" — list of snapshot dicts (epoch, diag_mass, xi, peak),
                   populated every cfg.monitor_every epochs if > 0
    """
    optimizer = torch.optim.AdamW(
        model.parameters(), lr=cfg.lr, weight_decay=cfg.weight_decay,
        betas=(0.9, 0.98),
    )
    scheduler = torch.optim.lr_scheduler.LambdaLR(
        optimizer, lr_lambda=lambda step: min(step / 10, 1.0)
    )
    history: Dict = {
        "train_loss": [], "val_loss": [],
        "train_acc":  [], "val_acc":  [],
        "spectral": [],
    }
    t0 = time.time()

    for epoch in range(cfg.epochs):
        model.train()
        ep_loss = ep_acc = n_batches = 0
        for x, y in train_loader:
            x, y = x.to(device), y.to(device)
            loss, acc = _loss_and_acc(model(x), y)
            optimizer.zero_grad()
            loss.backward()
            optimizer.step()
            scheduler.step()
            ep_loss += loss.item(); ep_acc += acc; n_batches += 1

        train_loss = ep_loss / n_batches
        train_acc  = ep_acc  / n_batches
        val_loss, val_acc = evaluate(model, val_loader)

        history["train_loss"].append(train_loss)
        history["val_loss"].append(val_loss)
        history["train_acc"].append(train_acc)
        history["val_acc"].append(val_acc)

        if cfg.monitor_every > 0 and epoch % cfg.monitor_every == 0:
            obs = a.monitor_spectral_observables(
                model=model, n=cfg.p, seq_len=cfg.seq_len,
                device=device, batch_size=cfg.batch_size,
                eq_token=cfg.p,
            )
            history["spectral"].append({
                "epoch":     epoch,
                "diag_mass": obs["diagonal_spectral_mass"],
                "xi":        obs["correlation_length"],
                "peak":      obs["peak_index"],
            })

        if (epoch + 1) % 100 == 0:
            print(
                f"Epoch {epoch + 1:5d}/{cfg.epochs}  "
                f"train loss {train_loss:.4f} acc {train_acc:.3f}  "
                f"val loss {val_loss:.4f} acc {val_acc:.3f}"
            )

    print(f"Training time: {time.time() - t0:.1f}s")
    return history



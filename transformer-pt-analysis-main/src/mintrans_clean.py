class MLP(nn.Module):
    def __init__(self, d_model: int, hidden_dim: int) -> None:
        super().__init__()
        self.net = nn.Sequential(
            nn.Linear(d_model, hidden_dim),
            nn.ReLU(),
            #nn.Linear(hidden_dim, hidden_dim),
            #nn.ReLU(),
            nn.Linear(hidden_dim, d_model),
        )

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        return self.net(x)


class MinimalTransformer(nn.Module):
    """
    Decoder-only transformer with causal self-attention, no layer norm.

    Follows the architecture in Nanda et al. (2023): token + positional
    embeddings fed into alternating attention and MLP residual blocks,
    projected to vocabulary logits.  Layer norm is omitted to keep the
    residual stream algebraically clean for mechanistic analysis.
    """

    def __init__(
        self,
        vocab_size: int,
        d_model: int = 128,
        n_heads: int = 4,
        num_layers: int = 1,
        max_seq_len: int = 20,
        hidden_mlp: int = 512,
    ) -> None:
        super().__init__()
        self.vocab_size = vocab_size
        self.d_model    = d_model
        self.token_embed = nn.Embedding(vocab_size, d_model)
        self.pos_embed   = nn.Embedding(max_seq_len, d_model)
        self.attn_layers = nn.ModuleList([
            nn.MultiheadAttention(d_model, n_heads, batch_first=True, dropout=0.0, bias=False)
            for _ in range(num_layers)
        ])
        self.mlp_layers = nn.ModuleList([
            MLP(d_model, hidden_mlp) for _ in range(num_layers)
        ])
        self.out_proj = nn.Linear(d_model, vocab_size)

        # Match Nanda's init: all weight matrices ~ N(0, 1/sqrt(d_model)), biases = 0
        std = 1.0 / math.sqrt(d_model)
        nn.init.normal_(self.token_embed.weight, std=std)
        nn.init.normal_(self.pos_embed.weight,   std=std)
        nn.init.normal_(self.out_proj.weight,    std=std)
        nn.init.zeros_(self.out_proj.bias)
        for attn in self.attn_layers:
            nn.init.normal_(attn.in_proj_weight, std=std)
            nn.init.normal_(attn.out_proj.weight, std=std)
        for mlp in self.mlp_layers:
            nn.init.normal_(mlp.net[0].weight, std=std)
            nn.init.zeros_(mlp.net[0].bias)
            nn.init.normal_(mlp.net[2].weight, std=std)
            nn.init.zeros_(mlp.net[2].bias)

    def forward(self, tokens: torch.Tensor) -> torch.Tensor:
        """tokens : LongTensor [B, T]  →  logits : FloatTensor [B, T, vocab_size]"""
        B, T = tokens.shape
        pos = torch.arange(T, device=tokens.device)
        x = self.token_embed(tokens) + self.pos_embed(pos).unsqueeze(0)
        causal_mask = torch.triu(
            torch.full((T, T), float("-inf"), device=tokens.device), diagonal=1
        )
        for attn, mlp in zip(self.attn_layers, self.mlp_layers):
            x = x + attn(x, x, x, attn_mask=causal_mask)[0]
            x = x + mlp(x)
        return self.out_proj(x)

    def token_embeddings(self) -> torch.Tensor:
        """W_E : FloatTensor [vocab_size, d_model] (detached)."""
        return self.token_embed.weight.detach()



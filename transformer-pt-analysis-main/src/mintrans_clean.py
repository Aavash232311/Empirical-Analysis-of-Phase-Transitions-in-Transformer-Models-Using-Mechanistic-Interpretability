def embedding_fourier_amplitudes(W_E: torch.Tensor, p: int = None) -> np.ndarray:
    """
    Decompose token embeddings along the discrete Fourier basis and return the
    per-frequency amplitude.

    W_E : FloatTensor [vocab_size, d_model]
    p   : if given, only the first p rows are used (excludes the "=" token)
    amplitude : ndarray [p // 2 + 1]  — L2 norm over d_model for each frequency
    """
    if p is not None:
        W_E = W_E[:p]
    F = torch.fft.rfft(W_E, dim=0, norm="ortho")   # [p//2+1, d_model]
    return F.abs().pow(2).sum(dim=-1).sqrt().cpu().numpy()


def plot_embedding_fourier_spectrum(model: MinimalTransformer, p: int = None) -> None:
    """
    Bar chart of Fourier amplitudes of the token embedding matrix W_E.

    A grokked model concentrates power at a small number of key frequencies.
    Use this to identify which frequencies to pass to
    plot_embedding_trig_components.

    p : if given, only the first p token rows are analysed (excludes "=" token)
    """
    amp   = embedding_fourier_amplitudes(model.token_embeddings().cpu(), p=p)
    freqs = np.arange(len(amp))

    plt.figure(figsize=(10, 4))
    plt.bar(freqs, amp, width=0.8)
    plt.xlabel("frequency index k")
    plt.ylabel("‖F_k‖₂  (summed over d_model)")
    plt.title("Fourier spectrum of token embeddings W_E")
    plt.tight_layout(); plt.show()


def plot_embedding_trig_components(
    model: MinimalTransformer, freq: int, p: int = None
) -> None:
    """
    Scatter-plot token embeddings projected onto the cosine and sine basis
    vectors for a given frequency.

    Nanda et al. show that after grokking token k maps to a point near
    (cos(2πf·k/p), sin(2πf·k/p)) for each key frequency f, i.e., the
    tokens are evenly spaced on a circle.  This plot lets you verify that.

    Parameters
    ----------
    model : trained MinimalTransformer
    freq  : Fourier frequency index to inspect (use plot_embedding_fourier_spectrum
            to identify dominant ones; skip DC at index 0)
    p     : if given, only the first p rows of W_E are used (excludes "=" token)
    """
    W_E = model.token_embeddings().cpu().numpy()   # [vocab_size, d]
    if p is not None:
        W_E = W_E[:p]
    p   = W_E.shape[0]
    k   = np.arange(p)

    cos_vec = np.cos(2 * np.pi * freq * k / p)
    sin_vec = np.sin(2 * np.pi * freq * k / p)
    cos_vec /= np.linalg.norm(cos_vec)
    sin_vec /= np.linalg.norm(sin_vec)

    cos_proj = W_E @ cos_vec
    sin_proj = W_E @ sin_vec

    fig, ax = plt.subplots(figsize=(5, 5))
    sc = ax.scatter(cos_proj, sin_proj, c=k, cmap="hsv", s=30, zorder=3)
    r  = np.hypot(cos_proj, sin_proj).mean()
    th = np.linspace(0, 2 * np.pi, 300)
    ax.plot(r * np.cos(th), r * np.sin(th), "k--", lw=0.8, alpha=0.4)

    plt.colorbar(sc, ax=ax, label="token index k")
    ax.set_aspect("equal")
    ax.set_xlabel(f"projection onto cos(2π·{freq}·k/p)")
    ax.set_ylabel(f"projection onto sin(2π·{freq}·k/p)")
    ax.set_title(f"Token embeddings — frequency {freq}")
    plt.tight_layout(); plt.show()


def plot_logit_structure_factor(model: MinimalTransformer, cfg: TrainConfig) -> None:
    """
    Compute and display the 2-D structure factor S(k, l) of the model's logit
    table at the current checkpoint.

    S(k, l) is the class-summed power spectrum on the pair-state lattice Z_p².
    After grokking the model learns the modular-addition rule and S concentrates
    on the diagonal k = l (see the phase-transition notes, §4–5).

    Shows two panels:
      Left  — log(1 + S(k, l)) as a heatmap (DC zeroed for visibility)
      Right — row-summed power vs diagonal power, with M_diag annotated
    """
    obs = a.monitor_spectral_observables(
        model=model, n=cfg.p, seq_len=cfg.seq_len,
        device=device, batch_size=cfg.batch_size,
        eq_token=cfg.p,
    )
    S = obs["structure_factor"].cpu().numpy()
    n = S.shape[0]

    S_display = S.copy()
    S_display[0, 0] = 0.0          # zero DC so colour scale is meaningful

    fig, (ax_map, ax_bar) = plt.subplots(1, 2, figsize=(13, 5))

    im = ax_map.imshow(
        np.log1p(S_display), origin="lower", cmap="inferno",
        extent=[0, n, 0, n],
    )
    ax_map.set_xlabel("ℓ (frequency in b-direction)")
    ax_map.set_ylabel("k (frequency in a-direction)")
    ax_map.set_title("log(1 + S(k, ℓ)) — full spectrum")
    plt.colorbar(im, ax=ax_map)

    idx = np.arange(n)
    ax_bar.plot(idx, S_display.sum(axis=1), label=r"row sum $\sum_ℓ S(k,ℓ)$")
    ax_bar.plot(idx, S_display[idx, idx],   label="diagonal S(k,k)")
    ax_bar.set_xlabel("k")
    ax_bar.set_ylabel("power")
    ax_bar.set_title(
        f"Diagonal spectral mass M_diag = {obs['diagonal_spectral_mass']:.3f}"
    )
    ax_bar.legend()

    plt.tight_layout(); plt.show()


def plot_trig_overview(model: MinimalTransformer, cfg: TrainConfig, top_k: int = 3) -> None:
    """
    Convenience wrapper: Fourier spectrum, structure factor heatmap, and
    trig-circle plots for the top-k dominant frequencies (DC excluded).
    """
    plot_logit_structure_factor(model, cfg)
    plot_embedding_fourier_spectrum(model, p=cfg.p)

    amp   = embedding_fourier_amplitudes(model.token_embeddings().cpu(), p=cfg.p)
    freqs = np.argsort(amp[1:])[::-1][:top_k] + 1   # skip DC at index 0
    for f in freqs:
        plot_embedding_trig_components(model, int(f), p=cfg.p)



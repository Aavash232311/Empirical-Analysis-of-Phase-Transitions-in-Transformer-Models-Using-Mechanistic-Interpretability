    ax_loss.semilogy(epochs, history["val_loss"],   label="val")
    ax_loss.set_xlabel("epoch"); ax_loss.set_ylabel("loss (log scale)")
    ax_loss.set_title(f"Loss{suffix}"); ax_loss.legend()

    ax_acc.plot(epochs, history["train_acc"], label="train")
    ax_acc.plot(epochs, history["val_acc"],   label="val")
    ax_acc.set_xlabel("epoch"); ax_acc.set_ylabel("accuracy")
    ax_acc.set_title(f"Accuracy{suffix}"); ax_acc.legend()

    plt.tight_layout(); plt.show()


